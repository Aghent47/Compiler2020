class Calculator:
    
    def sumar(self,numberOne,numbertwo):
        print ("El resultado de sumar: "+ str(numberOne)+" y "+str(numbertwo)+" es: "+str((numberOne+numbertwo)))

    def restar(self,numberOne,numbertwo):
        print ("El resultado de restar: "+str(numberOne)+" y "+str(numbertwo)+" es: "+str((numberOne-numbertwo)))

    def mult(self,numberOne,numbertwo):
        print ("El resultado de multiplicar: "+str(numberOne)+" y "+str(numbertwo)+" es: "+str((numberOne*numbertwo)))

    def div(self,numberOne,numbertwo):
        print ("El resultado de dividir: "+str(numberOne)+" y "+str(numbertwo)+" es: "+str((numberOne/numbertwo)))

